package colecoes;

import java.util.LinkedList;
import java.util.Queue;

public class Fila {
	
	public static void main(String[] args) {
		
		Queue<String> fila = new LinkedList<>();
		
		//Offer e Add -> adicionam elementros na fila
		//Diferença é o comportamento quando a fila está cheia!
		fila.add("Ana");
		fila.offer("Bia");
		fila.add("Carlos"); //Retorna uma exceção
		fila.offer("Daniel"); //Retorna false
		fila.add("Rafaela");
		fila.offer("Gui");
		
		/*
		 * Peek e Element -> obter o próximo elemento
		 * da fila
		 * 
		 * A diferença entre eles ocorram quando a fila está vazia
		 */
		
		System.out.println(fila.peek()); //Retorna null
		System.out.println(fila.peek());
		System.out.println(fila.element()); //Lança uma exceção
		System.out.println(fila.element());
		
		
		/*
		 * Poll e Remove -> obter o próximo elemento
		 * da fila e remove!
		 * 
		 * Diferença é o comportamento quando está vazia
		 */
		System.out.println(fila.poll());
		System.out.println(fila.remove());
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.remove());
		
		
		//fila.size();
		//fila.clear();
		//fila.isEmpty();
		//fila.contains(...);
	}

}
